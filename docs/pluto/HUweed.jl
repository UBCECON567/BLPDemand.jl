	# Are these the right way to calculate share and price? If not, correct it
	"""
	computeshare based on `var` in `df`. The size of the market is assumed by `scale`*`df.population`
	"""
	function computeshare(df,var;scale=1)
		sdf=combine(groupby(df,[:city,:date]),var => sum => :marketunits)
		maxdf=combine(groupby(sdf,[:city]),:marketunits => maximum => :marketunits_max)
		df = leftjoin(df,sdf,on=[:city,:date], matchmissing=:notequal)
		df = leftjoin(df,maxdf,on=:city, matchmissing=:notequal)
		df.share = df[!,var]./max.(scale*df.population, df.marketunits_max*1.1)
		sdf=combine(groupby(df,[:city,:date]),:share => (x->1-sum(x)) => :outside_share)
		df = leftjoin(df,sdf,on=[:city,:date], matchmissing=:notequal)	
		select!(df, Not([:marketunits, :marketunits_max]))
		return(df)
	end
	# share based on `units` from weekly data
	df=computeshare(weekly,:units)	
	rename!(df, :share => :share_units_avg_wk, :outside_share => :outside_share_units_avg_wk)

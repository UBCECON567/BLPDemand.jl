# Elasticity 

The price elasticity of demand is $\epsilon^D = \frac{\partial Q}{\partial p} \frac{p}{Q}$. In general, the demand function and its derivative depend on the coefficients estimated above, the value of the controls, the fixed effects, and the residuals. For the logit model, the elasticity simplifies somewhat because
```math
s_j(p) = \frac{e^{\alpha p_j + x_{jt} \beta + \delta_t + \psi_j + \epsilon_{jt}}}{1 + \sum_{k=1}^J e^{\alpha p_k + x_{kt} \beta + \delta_t + \psi_k + \epsilon_{kt}}}
```
and
```math
\frac{\partial s_j(p)}{\partial p_j} = \alpha s_j(p)(1-s_j(p))
```
so
```math
\epsilon^D = \alpha (1-s_j(p)) p_j
```

